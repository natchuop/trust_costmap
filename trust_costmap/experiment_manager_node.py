import os
from dataclasses import dataclass
from typing import Dict, List, Optional

import yaml

import rclpy
from rclpy.node import Node
from ament_index_python.packages import get_package_share_directory

from nav_msgs.msg import OccupancyGrid
from visualization_msgs.msg import Marker, MarkerArray


@dataclass
class RobotConfig:
    robot_id: str
    role: str
    model: str
    start_cell: List[int]
    goal_cell: Optional[List[int]]
    enabled: bool


@dataclass
class MovingAIMap:
    name: str
    map_type: str
    height: int
    width: int
    grid: List[str]

    def count_free(self) -> int:
        free_symbols = {".", "G", "S"}
        return sum(1 for row in self.grid for ch in row if ch in free_symbols)

    def count_blocked(self) -> int:
        free_symbols = {".", "G", "S"}
        return sum(1 for row in self.grid for ch in row if ch not in free_symbols)


class ExperimentManagerNode(Node):
    """
    Main experiment runner scaffold.

    Current responsibility:
    - load scenario.yaml
    - load selected MovingAI benchmark map from worlds/movingai_mapf/
    - load any number of robots from the scenario file
    - publish /base_map as nav_msgs/OccupancyGrid
    - publish /robot_markers as visualization_msgs/MarkerArray
    - publish /start_goal_markers as visualization_msgs/MarkerArray

    Gazebo visuals are generated by experiment.launch.py for now.
    RViz debug visuals are published here.
    """

    def __init__(self):
        super().__init__("experiment_manager")

        self.declare_parameter("map_name", "room-32-32-4")
        self.declare_parameter("scenario_file", "scenario.yaml")

        self.map_name = (
            self.get_parameter("map_name")
            .get_parameter_value()
            .string_value
        )

        self.scenario_file = (
            self.get_parameter("scenario_file")
            .get_parameter_value()
            .string_value
        )

        self.package_share = get_package_share_directory("trust_costmap")

        self.get_logger().info("Experiment manager starting.")
        self.get_logger().info(f"Selected map_name: {self.map_name}")
        self.get_logger().info(f"Selected scenario_file: {self.scenario_file}")

        self.scenario = self.load_scenario(self.scenario_file)
        self.visualization_config = self.scenario.get("visualization", {})
        self.cell_size_m = float(self.visualization_config.get("cell_size_m", 0.5))

        self.map_data = self.load_map_by_name(self.map_name)
        self.robots = self.load_robot_configs(self.scenario)

        self.base_map_pub = self.create_publisher(
            OccupancyGrid,
            "/base_map",
            10,
        )

        self.robot_markers_pub = self.create_publisher(
            MarkerArray,
            "/robot_markers",
            10,
        )

        self.start_goal_markers_pub = self.create_publisher(
            MarkerArray,
            "/start_goal_markers",
            10,
        )

        self.print_startup_summary()

        self.timer = self.create_timer(1.0, self.publish_visual_debug)

    def load_scenario(self, scenario_file: str) -> Dict:
        scenario_path = os.path.join(self.package_share, scenario_file)

        if not os.path.exists(scenario_path):
            raise FileNotFoundError(
                f"Scenario file not found: {scenario_path}. "
                "Make sure scenario.yaml exists in the package root and is installed by setup.py."
            )

        with open(scenario_path, "r", encoding="utf-8") as f:
            data = yaml.safe_load(f)

        if data is None:
            raise ValueError(f"Scenario file is empty: {scenario_path}")

        return data

    def load_map_by_name(self, map_name: str) -> MovingAIMap:
        map_path = os.path.join(
            self.package_share,
            "worlds",
            "movingai_mapf",
            f"{map_name}.map",
        )

        if not os.path.exists(map_path):
            raise FileNotFoundError(
                f"Map file not found: {map_path}. "
                f"Expected worlds/movingai_mapf/{map_name}.map"
            )

        return self.load_movingai_map(map_path, map_name)

    def load_movingai_map(self, path: str, map_name: str) -> MovingAIMap:
        with open(path, "r", encoding="utf-8") as f:
            lines = [line.rstrip("\n") for line in f]

        map_type = None
        height = None
        width = None
        map_start_index = None

        for i, line in enumerate(lines):
            clean = line.strip()

            if clean.startswith("type "):
                map_type = clean.split()[1]
            elif clean.startswith("height "):
                height = int(clean.split()[1])
            elif clean.startswith("width "):
                width = int(clean.split()[1])
            elif clean == "map":
                map_start_index = i + 1
                break

        if map_type is None:
            raise ValueError(f"Missing type field in map: {path}")

        if height is None:
            raise ValueError(f"Missing height field in map: {path}")

        if width is None:
            raise ValueError(f"Missing width field in map: {path}")

        if map_start_index is None:
            raise ValueError(f"Missing map grid marker in map: {path}")

        grid = lines[map_start_index:map_start_index + height]

        if len(grid) != height:
            raise ValueError(
                f"Expected {height} grid rows, got {len(grid)} in {path}"
            )

        for row_index, row in enumerate(grid):
            if len(row) != width:
                raise ValueError(
                    f"Map row {row_index} expected width {width}, got {len(row)}"
                )

        return MovingAIMap(
            name=map_name,
            map_type=map_type,
            height=height,
            width=width,
            grid=grid,
        )

    def load_robot_configs(self, scenario: Dict) -> List[RobotConfig]:
        robots_raw = scenario.get("robots", [])

        if not robots_raw:
            raise ValueError("Scenario must define at least one robot.")

        robots = []

        for item in robots_raw:
            robot_id = str(item["id"])
            role = str(item["role"])
            model = str(item.get("model", "simple_box_robot"))
            start_cell = list(item["start_cell"])
            goal_cell = item.get("goal_cell", None)
            enabled = bool(item.get("enabled", True))

            if goal_cell is not None:
                goal_cell = list(goal_cell)

            robots.append(
                RobotConfig(
                    robot_id=robot_id,
                    role=role,
                    model=model,
                    start_cell=start_cell,
                    goal_cell=goal_cell,
                    enabled=enabled,
                )
            )

        return robots

    def is_free_cell(self, row: int, col: int) -> bool:
        ch = self.map_data.grid[row][col]
        return ch in [".", "G", "S"]

    def is_blocked_cell(self, row: int, col: int) -> bool:
        return not self.is_free_cell(row, col)

    def cell_to_world(self, row: int, col: int):
        x = (col + 0.5) * self.cell_size_m
        y = (row + 0.5) * self.cell_size_m
        return x, y

    def validate_robot_cells(self):
        for robot in self.robots:
            if not robot.enabled:
                continue

            sr, sc = int(robot.start_cell[0]), int(robot.start_cell[1])

            if sr < 0 or sr >= self.map_data.height or sc < 0 or sc >= self.map_data.width:
                self.get_logger().warn(
                    f"Robot {robot.robot_id} start_cell out of bounds: {robot.start_cell}"
                )
                continue

            if self.is_blocked_cell(sr, sc):
                self.get_logger().warn(
                    f"Robot {robot.robot_id} starts on a blocked cell: {robot.start_cell}. "
                    "It will still be visualized, but this should be fixed."
                )

            if robot.goal_cell is not None:
                gr, gc = int(robot.goal_cell[0]), int(robot.goal_cell[1])

                if gr < 0 or gr >= self.map_data.height or gc < 0 or gc >= self.map_data.width:
                    self.get_logger().warn(
                        f"Robot {robot.robot_id} goal_cell out of bounds: {robot.goal_cell}"
                    )
                    continue

                if self.is_blocked_cell(gr, gc):
                    self.get_logger().warn(
                        f"Robot {robot.robot_id} goal_cell is blocked: {robot.goal_cell}. "
                        "Planning will not like this later, because apparently walls remain rude."
                    )

    def publish_visual_debug(self):
        self.publish_base_map()
        self.publish_robot_markers()
        self.publish_start_goal_markers()

    def publish_base_map(self):
        msg = OccupancyGrid()
        msg.header.stamp = self.get_clock().now().to_msg()
        msg.header.frame_id = "map"

        msg.info.resolution = self.cell_size_m
        msg.info.width = self.map_data.width
        msg.info.height = self.map_data.height

        msg.info.origin.position.x = 0.0
        msg.info.origin.position.y = 0.0
        msg.info.origin.position.z = 0.0
        msg.info.origin.orientation.w = 1.0

        data = []

        for row in range(self.map_data.height):
            for col in range(self.map_data.width):
                if self.is_blocked_cell(row, col):
                    data.append(100)
                else:
                    data.append(0)

        msg.data = data
        self.base_map_pub.publish(msg)

    def marker_color_for_role(self, marker, role: str):
        marker.color.a = 1.0

        if role == "malicious_reporter":
            marker.color.r = 0.9
            marker.color.g = 0.05
            marker.color.b = 0.05
        elif role == "benign_reporter":
            marker.color.r = 0.05
            marker.color.g = 0.25
            marker.color.b = 0.9
        else:
            marker.color.r = 0.05
            marker.color.g = 0.75
            marker.color.b = 0.25

    def publish_robot_markers(self):
        markers = MarkerArray()

        marker_id = 0

        for robot in self.robots:
            if not robot.enabled:
                continue

            row = int(robot.start_cell[0])
            col = int(robot.start_cell[1])
            x, y = self.cell_to_world(row, col)

            marker = Marker()
            marker.header.stamp = self.get_clock().now().to_msg()
            marker.header.frame_id = "map"
            marker.ns = "robots"
            marker.id = marker_id
            marker.type = Marker.CUBE
            marker.action = Marker.ADD

            marker.pose.position.x = x
            marker.pose.position.y = y
            marker.pose.position.z = 0.15
            marker.pose.orientation.w = 1.0

            marker.scale.x = self.cell_size_m * 0.7
            marker.scale.y = self.cell_size_m * 0.7
            marker.scale.z = 0.3

            self.marker_color_for_role(marker, robot.role)

            markers.markers.append(marker)
            marker_id += 1

            label = Marker()
            label.header.stamp = self.get_clock().now().to_msg()
            label.header.frame_id = "map"
            label.ns = "robot_labels"
            label.id = marker_id
            label.type = Marker.TEXT_VIEW_FACING
            label.action = Marker.ADD

            label.pose.position.x = x
            label.pose.position.y = y
            label.pose.position.z = 0.7
            label.pose.orientation.w = 1.0

            label.scale.z = 0.25
            label.color.a = 1.0
            label.color.r = 1.0
            label.color.g = 1.0
            label.color.b = 1.0
            label.text = robot.robot_id

            markers.markers.append(label)
            marker_id += 1

        self.robot_markers_pub.publish(markers)

    def publish_start_goal_markers(self):
        markers = MarkerArray()
        marker_id = 0

        for robot in self.robots:
            if not robot.enabled:
                continue

            start_row = int(robot.start_cell[0])
            start_col = int(robot.start_cell[1])
            start_x, start_y = self.cell_to_world(start_row, start_col)

            start_marker = Marker()
            start_marker.header.stamp = self.get_clock().now().to_msg()
            start_marker.header.frame_id = "map"
            start_marker.ns = "starts"
            start_marker.id = marker_id
            start_marker.type = Marker.SPHERE
            start_marker.action = Marker.ADD
            start_marker.pose.position.x = start_x
            start_marker.pose.position.y = start_y
            start_marker.pose.position.z = 0.45
            start_marker.pose.orientation.w = 1.0
            start_marker.scale.x = self.cell_size_m * 0.45
            start_marker.scale.y = self.cell_size_m * 0.45
            start_marker.scale.z = self.cell_size_m * 0.45
            start_marker.color.a = 1.0
            start_marker.color.r = 0.0
            start_marker.color.g = 1.0
            start_marker.color.b = 0.0
            markers.markers.append(start_marker)
            marker_id += 1

            if robot.goal_cell is not None:
                goal_row = int(robot.goal_cell[0])
                goal_col = int(robot.goal_cell[1])
                goal_x, goal_y = self.cell_to_world(goal_row, goal_col)

                goal_marker = Marker()
                goal_marker.header.stamp = self.get_clock().now().to_msg()
                goal_marker.header.frame_id = "map"
                goal_marker.ns = "goals"
                goal_marker.id = marker_id
                goal_marker.type = Marker.SPHERE
                goal_marker.action = Marker.ADD
                goal_marker.pose.position.x = goal_x
                goal_marker.pose.position.y = goal_y
                goal_marker.pose.position.z = 0.45
                goal_marker.pose.orientation.w = 1.0
                goal_marker.scale.x = self.cell_size_m * 0.5
                goal_marker.scale.y = self.cell_size_m * 0.5
                goal_marker.scale.z = self.cell_size_m * 0.5
                goal_marker.color.a = 1.0
                goal_marker.color.r = 1.0
                goal_marker.color.g = 0.8
                goal_marker.color.b = 0.0
                markers.markers.append(goal_marker)
                marker_id += 1

        self.start_goal_markers_pub.publish(markers)

    def print_startup_summary(self):
        self.validate_robot_cells()

        scenario_name = self.scenario.get("scenario_name", "unnamed_scenario")
        experiment = self.scenario.get("experiment", {})
        evaluation = self.scenario.get("evaluation", {})

        free_count = self.map_data.count_free()
        blocked_count = self.map_data.count_blocked()
        total_count = self.map_data.width * self.map_data.height

        self.get_logger().info("")
        self.get_logger().info("========== Experiment Summary ==========")
        self.get_logger().info(f"Scenario: {scenario_name}")
        self.get_logger().info(f"Method: {experiment.get('method', 'unknown')}")
        self.get_logger().info(f"Duration: {experiment.get('duration_sec', 'unknown')} sec")
        self.get_logger().info(f"Update rate: {experiment.get('update_rate_hz', 'unknown')} Hz")
        self.get_logger().info("")
        self.get_logger().info("Map:")
        self.get_logger().info(f"  name: {self.map_data.name}")
        self.get_logger().info(f"  type: {self.map_data.map_type}")
        self.get_logger().info(f"  size: {self.map_data.width} x {self.map_data.height}")
        self.get_logger().info(f"  total cells: {total_count}")
        self.get_logger().info(f"  free cells: {free_count}")
        self.get_logger().info(f"  blocked cells: {blocked_count}")
        self.get_logger().info(f"  cell size: {self.cell_size_m} m")
        self.get_logger().info("")
        self.get_logger().info("Robots:")

        for robot in self.robots:
            self.get_logger().info(
                f"  {robot.robot_id}: "
                f"role={robot.role}, "
                f"model={robot.model}, "
                f"start_cell={robot.start_cell}, "
                f"goal_cell={robot.goal_cell}, "
                f"enabled={robot.enabled}"
            )

        self.get_logger().info("")
        self.get_logger().info(
            f"Primary robot: {evaluation.get('primary_robot', 'not set')}"
        )

        self.get_logger().info("========================================")
        self.get_logger().info("")

        preview_rows = min(8, self.map_data.height)
        self.get_logger().info("Map preview:")
        for row in self.map_data.grid[:preview_rows]:
            self.get_logger().info(row[:120])


def main(args=None):
    rclpy.init(args=args)
    node = ExperimentManagerNode()
    rclpy.spin(node)
    node.destroy_node()
    rclpy.shutdown()
